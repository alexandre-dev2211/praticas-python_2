# Lendo dez números inteiros e realizando a média:

lista = list()

n = 0

for n in range(1, 11):
    n + 1
    lista.append(n)
    
print("Este são os números da lista: ", lista)

sum = sum(lista)

lenght = len(lista)

mid = sum / lenght

print("Esta é a soma da lista: ", sum)

print("Esta é a média da lista: ", mid)
